#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (fd < 0 || fd > 1024)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}
